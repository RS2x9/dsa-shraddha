/*
        bitwise |  : it's bitwise OR
        this also retrn decimal value after operation 
        0 | 0 --> 0
        1 | 0 --> 1
        0 | 1 --> 1
        1 & 1 --> 1
*/

#include<bits/stdc++.h>
using namespace std;
int main()
{
    cout <<(4 | 5)<<endl;   // 0100 | 0101 --> 0101  --> 5 in decimal 

}